package se.codeslasher.docker.docker_api_1_24.container;

import com.github.tomakehurst.wiremock.http.RequestMethod;
import com.github.tomakehurst.wiremock.junit.WireMockRule;
import com.github.tomakehurst.wiremock.matching.RequestPatternBuilder;
import com.github.tomakehurst.wiremock.matching.UrlPattern;
import org.junit.After;
import org.junit.Before;
import org.junit.Rule;
import org.junit.Test;
import se.codeslasher.docker.DefaultDockerClient;
import se.codeslasher.docker.DockerClient;

import static com.github.tomakehurst.wiremock.core.WireMockConfiguration.options;

public class ContainerWait {

    private DockerClient client;

    @Rule
    public WireMockRule wireMockRule = new WireMockRule(options().usingFilesUnderClasspath("src/test/resources/1_24").port(9779)); // No-args constructor defaults

    @Before
    public void setup() {
        client = new DefaultDockerClient("http://127.0.0.1:9779");
    }

    @After
    public void tearDown() {
        client.close();
    }

    @Test
    public void waitForStop() {
        final String waitPath = "/v1.24%2Fcontainers%2Fmongo%2Fwait";

        client.waitForContainerStop("mongo");

        UrlPattern waitPattern = UrlPattern.fromOneOf(waitPath, null, null, null);
        RequestPatternBuilder waitRequestPatternBuilder = RequestPatternBuilder.newRequestPattern(RequestMethod.POST,waitPattern);
        wireMockRule.verify(1, waitRequestPatternBuilder);
    }

}
